//Function with Parameters

//Fuction definition
function calculateArea(length,width) {
    return length*width;
}

//calling above function for different values
console.log(`Area of rectangle is : ${calculateArea(4,5)}`);
console.log(`Area of rectangle is : ${calculateArea(5.5,2)}`);
console.log(`Area of rectangle is : ${calculateArea(10.3,6.4)}`);